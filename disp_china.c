// Set page address 0~15
void Set_Page_Address(uint8_t add)
{
    add = 0xb0 | add;
    Write_Instruction(add);
	return;
}
// Set column address
void Set_Column_Address(uint8_t add)
{
    Write_Instruction((0x10 | (add>>4)));
	Write_Instruction((0x0f & add));
	return;
}

